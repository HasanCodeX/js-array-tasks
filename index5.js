// Step 1: Create two arrays
const numbers1 = [1, 2, 3, 4];
const numbers2 = [5, 6, 7, 8];

// Step 2: Use concat to combine the arrays
const combinedArray = numbers1.concat(numbers2);

// Step 3: Print original and combined arrays
console.log('Array 1:', numbers1); // Output: [1, 2, 3, 4]
console.log('Array 2:', numbers2); // Output: [5, 6, 7, 8]
console.log('Combined Array:', combinedArray); // Output: [1, 2, 3, 4, 5, 6, 7, 8]
